package io.gravitee.am.repository.mongodb.gateway.internal.model;

import io.gravitee.am.model.ReferenceType;
import org.bson.codecs.pojo.annotations.BsonId;

import java.util.Date;
import java.util.List;

/**
 * @author GraviteeSource Team
 */
public class CommandStagingMongo {
    @BsonId
    private String id;
    private String command;
    private String userId;
    private ReferenceType referenceType;
    private String referenceId;
    private int attempts;
    private List<String> terminalClientIds;
    private Date createdAt;
    private Date updatedAt;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public ReferenceType getReferenceType() {
        return referenceType;
    }

    public void setReferenceType(ReferenceType referenceType) {
        this.referenceType = referenceType;
    }

    public String getReferenceId() {
        return referenceId;
    }

    public void setReferenceId(String referenceId) {
        this.referenceId = referenceId;
    }

    public int getAttempts() {
        return attempts;
    }

    public void setAttempts(int attempts) {
        this.attempts = attempts;
    }

    public List<String> getTerminalClientIds() {
        return terminalClientIds;
    }

    public void setTerminalClientIds(List<String> terminalClientIds) {
        this.terminalClientIds = terminalClientIds;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }
}
