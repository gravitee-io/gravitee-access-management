package io.gravitee.am.management.handlers.management.api.model;

import io.gravitee.am.model.Application;

/**
 * @author Titouan COMPIEGNE (titouan.compiegne at graviteesource.com)
 * @author GraviteeSource Team
 */
public class ApplicationEntity {

    private String id;
    private String clientId;
    private String name;

    public ApplicationEntity(String id, String clientId, String name) {
        this.id = id;
        this.clientId = clientId;
        this.name = name;
    }

    public ApplicationEntity(Application application) {
        setId(application.getId());
        setClientId((application.getSettings() != null && application.getSettings().getOauth() != null) ? application.getSettings().getOauth().getClientId() : null);
        setName(application.getName());
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
