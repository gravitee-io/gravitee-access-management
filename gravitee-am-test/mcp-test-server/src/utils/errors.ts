/**
 * Custom error classes
 * 
 * Defines application-specific error types with HTTP status codes and error codes
 * for consistent error handling across the application.
 */

export class AppError extends Error {
  constructor(
    message: string,
    public readonly statusCode: number = 500,
    public readonly code?: string,
  ) {
    super(message);
    this.name = this.constructor.name;
    Error.captureStackTrace(this, this.constructor);
  }
}

export class ValidationError extends AppError {
  constructor(message: string) {
    super(message, 400, 'VALIDATION_ERROR');
  }
}

export class NotFoundError extends AppError {
  constructor(message: string = 'Resource not found') {
    super(message, 404, 'NOT_FOUND');
  }
}

export class ConflictError extends AppError {
  constructor(message: string) {
    super(message, 409, 'CONFLICT');
  }
}

export class UnauthorizedError extends AppError {
  constructor(message: string = 'Unauthorized') {
    super(message, 401, 'UNAUTHORIZED');
  }
}

export class ForbiddenError extends AppError {
  constructor(message: string = 'Forbidden') {
    super(message, 403, 'FORBIDDEN');
  }
}

export class AuthZenAuthenticationError extends AppError {
  constructor(message: string = 'AuthZen authentication failed') {
    super(message, 401, 'AUTHZEN_AUTH_FAILED');
  }
}

export class AuthZenRequestError extends AppError {
  constructor(message: string, statusCode: number = 500) {
    super(message, statusCode, 'AUTHZEN_REQUEST_FAILED');
  }
}
