package io.gravitee.am.gateway.handler.common.policy;

import io.gravitee.am.common.policy.ExtensionPoint;
import io.gravitee.am.common.utils.ConstantKeys;
import io.gravitee.am.common.jwt.JWT;
import io.gravitee.am.gateway.handler.common.jwt.JWTService;
import io.gravitee.am.gateway.handler.common.vertx.utils.UriBuilderRequest;
import io.gravitee.am.model.Template;
import io.gravitee.am.model.oidc.Client;
import io.gravitee.am.service.utils.vertx.RequestUtils;
import io.gravitee.common.http.HttpMethod;
import io.gravitee.gateway.api.ExecutionContext;
import io.gravitee.gateway.api.Request;
import io.gravitee.gateway.api.Response;
import io.gravitee.policy.api.PolicyChain;
import io.gravitee.policy.api.PolicyResult;
import io.gravitee.policy.api.annotations.OnRequest;
import io.vertx.core.AsyncResult;
import io.vertx.core.Future;
import io.vertx.core.Handler;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.time.Instant;
import java.util.HashMap;
import java.util.Map;

/**
 * Abstract base class for User Action policies.
 * 
 * This class provides the default implementation for handling user action logic with built-in support for:
 * - HTTP redirects when a redirect URI is configured
 * - Error handling and exit-on-error configuration
 *
 * Developers should extend this class to implement custom user action business logic.
 *
 * @author Titouan COMPIEGNE (titouan.compiegne at graviteesource.com)
 * @author GraviteeSource Team
 */
public abstract class AbstractUserActionPolicy implements UserActionPolicy {

    private static final Logger LOGGER = LoggerFactory.getLogger(AbstractUserActionPolicy.class);
    private static final String GATEWAY_POLICY_USER_ACTION_ERROR_KEY = "GATEWAY_POLICY_USER_ACTION_ERROR";
    private static final String ERROR_MESSAGE = "Unable to execute user action";
    private static final String USER_ACTION_PATH = "/userAction";
    private static final String CONTEXT_PATH_KEY = "contextPath";
    private static final String ACTION_KEY = "action";
    private static final String FLOW_KEY = "flow";
    /**
     * Query parameter name used to transport a signed state containing action/flow info.
     * The token is generated by {@link #redirectUser} and later decoded by {@code UserActionEndpoint}.
     */
    private static final String UA_STATE_KEY = "ua_state";

    @OnRequest
    public void onRequest(Request request, Response response, ExecutionContext context, PolicyChain policyChain) {
        LOGGER.debug("Start AbstractUserActionPolicy.onRequest");
        try {
            // we only apply this policy on POST_* flow
            if (!request.method().equals(HttpMethod.POST)) {
                policyChain.failWith(PolicyResult.failure(GATEWAY_POLICY_USER_ACTION_ERROR_KEY, "User Action policy requires POST method"));
                return ;
            }

            final String flow = getFlow(request);
            if (flow == null) {
                policyChain.failWith(PolicyResult.failure(GATEWAY_POLICY_USER_ACTION_ERROR_KEY, "User Action policy requires a compatible FLOW"));
                return ;
            }

            final Client client = (Client) context.getAttribute(ConstantKeys.CLIENT_CONTEXT_KEY);
            redirectUser(client, flow, request, context, h -> {
                if (h.failed()) {
                    policyChain.failWith(PolicyResult.failure(GATEWAY_POLICY_USER_ACTION_ERROR_KEY, h.cause().getMessage()));
                    return;
                }
                // redirect the user
                policyChain.failWith(PolicyResult.failure(GATEWAY_POLICY_USER_ACTION_ERROR_KEY, 302, "User Action is required", h.result()));
            });
        } catch (Exception e) {
            policyChain.failWith(PolicyResult.failure(GATEWAY_POLICY_USER_ACTION_ERROR_KEY, ERROR_MESSAGE));
        }
    }

    private void redirectUser(
            Client client,
            String flow,
            Request request,
            ExecutionContext context,
            Handler<AsyncResult<Map<String, Object>>> handler) {

        final Map<String, String> requestParameters = new HashMap<>();
        RequestUtils.getQueryParams(request.uri()).forEach(requestParameters::put);

        final String action = getAction();
        // create ua_state
        createState(context, client, flow, action, h -> {
            if (h.failed()) {
                handler.handle(Future.failedFuture(h.cause()));
                return;
            }

            requestParameters.put(UA_STATE_KEY, h.result());
            final String requestPath = context.getAttribute(CONTEXT_PATH_KEY) + USER_ACTION_PATH;
            final String redirectTo = UriBuilderRequest.resolveProxyRequest(request, requestPath, requestParameters, true);
            final Map<String, Object> parameters = new HashMap<>();
            parameters.put(ConstantKeys.RETURN_URL_KEY, redirectTo);

            handler.handle(Future.succeededFuture(parameters));
        });
    }

    private static String getFlow(Request request) {
        final String requestUri = request.uri();
        if (requestUri.contains(Template.IDENTIFIER_FIRST_LOGIN.redirectUri())) {
            return ExtensionPoint.POST_LOGIN_IDENTIFIER.name();
        } else if (requestUri.contains(Template.LOGIN.redirectUri())) {
            return ExtensionPoint.POST_LOGIN.name();
        } else if (requestUri.contains(Template.OAUTH2_USER_CONSENT.redirectUri())) {
            return ExtensionPoint.POST_CONSENT.name();
        } else if (requestUri.contains(Template.REGISTRATION.redirectUri())) {
                    return ExtensionPoint.POST_REGISTER.name();
        } else if (requestUri.contains(Template.WEBAUTHN_REGISTER.redirectUri())) {
                    return ExtensionPoint.POST_WEBAUTHN_REGISTER.name();
        } else if (requestUri.contains(Template.MFA_CHALLENGE.redirectUri())) {
                    return ExtensionPoint.POST_MFA_CHALLENGE.name();
        } else if (requestUri.contains(Template.MFA_ENROLL.redirectUri())) {
            return ExtensionPoint.POST_MFA_ENROLLMENT.name();
        }
        return null;
    }

    private void createState(ExecutionContext context,
                               Client client,
                               String flow,
                               String action,
                               Handler<AsyncResult<String>> handler) {
        JWT jwt = new JWT();
        jwt.setIat(Instant.now().getEpochSecond());
        jwt.setExp(Instant.now().plusSeconds(300).getEpochSecond()); // 5 minutes validity
        jwt.setDomain(client.getDomain());
        jwt.setSub(client.getClientId());
        jwt.put(FLOW_KEY, flow);
        jwt.put(ACTION_KEY, action);
        JWTService jwtService = context.getComponent(JWTService.class);
        if (jwtService != null) {
            jwtService.encode(jwt, client)
                    .subscribe(
                            encodedJwt -> handler.handle(Future.succeededFuture(encodedJwt)),
                            throwable -> handler.handle(Future.failedFuture(throwable)));
        } else {
            handler.handle(Future.failedFuture(new IllegalStateException("JWTService is unavailable")));
        }
    }
}